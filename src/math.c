#include <stdlib.h>

// generated in C#: sin_table[i] = (byte)(128f * float.Sin(2.0f * MathF.PI * i / 256f) + 128f);
const uint8_t sin_table[64] =
{
	0x80, 0x83, 0x86, 0x89, 0x8C, 0x8F, 0x92, 0x95, 0x98, 0x9C, 0x9F, 0xA2, 0xA5, 0xA8, 0xAB, 0xAE,
	0xB0, 0xB3, 0xB6, 0xB9, 0xBC, 0xBF, 0xC1, 0xC4, 0xC7, 0xC9, 0xCC, 0xCE, 0xD1, 0xD3, 0xD5, 0xD8,
	0xDA, 0xDC, 0xDE, 0xE0, 0xE2, 0xE4, 0xE6, 0xE8, 0xEA, 0xEC, 0xED, 0xEF, 0xF0, 0xF2, 0xF3, 0xF5,
	0xF6, 0xF7, 0xF8, 0xF9, 0xFA, 0xFB, 0xFC, 0xFC, 0xFD, 0xFE, 0xFE, 0xFF, 0xFF, 0xFF, 0xFF, 0xFF
};

// get approximate 8-bit sine value
uint8_t sin(uint8_t x)
{
    if (x >= 192) return (256 - sin_table[255 - x]);
    else if (x >= 128) return (256 - sin_table[x - 128]);
    else if (x >= 64) return sin_table[127 - x];
    else return sin_table[x];
}
